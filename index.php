        <h1><span> Benefits of Blood Donation</span></h1>
        <P>Every year, nearly 5 million people receive life-saving blood transfusions, a process which would not be possible without active donors.  Blood banks rely on the donations of all different blood types in order to remain fully stocked and ready when blood is needed by anyone for any reason from accident and injury to surgery.  Furthermore, the act of donating blood provides a multitude of benefits to the donor.  Here are a few:</P>
        <ol>
        <li> 
            <h2>Your Blood Could Save Multiple Lives</h2>
        </li>
            <p>Most people envision their blood going to a single individual of the same blood type.  But, in reality, one donation could save up to three lives!  There are times when patients need a transfusion of whole blood, but there are others where only certain components of blood are needed.  For this reason, blood banks are able to extend the usefulness of each donation by separating them into red cells, platelets, and plasma, each of which could potentially go to a different patient.</p>
            <li>
        <h2>
            You’ll get a Check of Your Health with Blood Donation
        </h2>
            </li>
        <p>
            Before giving blood, each donor must undergo a minor health check that includes pulse, blood pressure, body temperature, and iron levels.  But, it doesn’t end there.  After your blood has been collected, it is sent to a lab where further testing is conducted.  In total, your blood will be checked for 13 infectious diseases such as West Nile and HIV, and should any of these tests return a positive result, you will be notified immediately.

        </p>
        <li>
        <h2>
            Blood Donation can Improve Your Overall Health
        </h2>
        </li>
        <p>
            Not only does donating blood help save lives, help monitor your health, and make you feel good, it could also offer some long-term benefits to your physical health.  The process has been shown capable of helping improve blood flow in some, resulting in healthier blood vessels and a lower likelihood of arterial blockages.  Likewise, the regulation and balance of iron levels that comes from blood donation is another perk leading to stronger blood vessels.  Perhaps this is why research has indicated that blood donors are 88 percent less likely than others to suffer a heart attack!
            
