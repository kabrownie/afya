        <h2>
            Blood Donation Types
        </h2>
        <ol>
            <li>
        <h3>
            Whole Blood Donation
        </h3></li>
        <p>
        A whole blood (WB) donation is the most common form of blood donation, in which a person gives one pint of blood.
        </p>
        <h4>
            Requirements for Whole Blood Donation
        </h4>
        <ul>
        <li>Donation frequency: Every 56 days, up to 6 times a year</li>
        <li> must be in good health and feeling well</li>
        <li> must be at least 16 years </li>
        <li>You must weigh at least 50kg</li>
        </ul>
        <li>
            <h3>
                Platelet Donation
            </h3>
        </li>
        <p>
            Is the donation of blood platelets.
        </p>
            <h5>
                What are platelets used for ?
            </h5>
            <p>
        Platelets, which come from bone marrow, help to induce clotting and control bleeding. Patients who have been treated for leukemia with chemotherapy, for example, will often need to be transfused platelets.
        </p>
        <!-- <h5>
             is the process of donating platelets
         </h5> -->
         <p> 
             During a platelet donation, we will collect one, two, or three units of platelets. One of our medical technicians will recommend the number of units based on your weight, hemoglobin level and platelet count that day.
         </p>
         <h4>
             Requirements for Platelets Donation
         </h4>
         <ul>
            
             <li>
             Donation frequency: Every 7 days, up to 24 times/year</li>
             <li>
                 You must be in good health and feeling well
             </li>
             <li> 
                must be at least 17 years 
             </li>
             <li>
                 You must weigh at least 50kgs
             </li>
         </ul>
         <li><h3> AB Elite Plasma Donation</h3></li>
         <p>
             This is donation of plasma by people with blood type AB+ or AB-

         </p>
         <h5>
             What is Plasma used for ?
         </h5>
         <p>
         Much like platelets, plasma helps to induce clotting and control bleeding. However, it does this through different mechanisms and many of the clotting factors found in plasma are made from the liver.  Therefore, although both plasma and platelets both help to control bleeding, they are sometimes used for different patient populations, depending on what the patient’s underlying disease.
         </p>
         <h4>
             Requiremets for AB Elite Plasma Donation 
         </h4>
         <ul>
             <li>Donation frequency: Every 28 days, up to 13 times/year</li>
             <li>You must have type AB blood</li>
             <li>
                  must be in good health and feeling well
             </li>
             <li>
                 You must be at least 17 years old
             </li>
             <li> 
                 must weigh at least 50 Kgs
             </li>
         </ul>
         <li>
             <h3>
                 Power Red or Double Red Blood Cell Donation (DRBC)
                </h3>
            </li>
         <h5>
             What is double red blood cell donation?
             </h5>
         <p> 
             A double red blood cell donation, a machine is used to withdraw whole blood. Red cells are separated and retained, and the remainder of the blood is returned back to the donor. This donation safely removes twice as many red cells than traditional whole blood. We refer to this as a double red blood cell donation, or DRBC for short. 
         </p>
         <h5>
             What are red blood cells used for? 
         </h5>
         <p>
             Red blood cells transport oxygen and nutrients to the tissues and remove carbon dioxide and waste. Red blood cells are indicated for more than 70% of all transfusions. Patients who lose or at risk of losing significant volumes of blood require red blood cell transfusions.  This population includes patients who have suffered severe trauma, have a perforated bleeding ulcer, or who are undergoing a major surgical procedure.

         </p> 
         <h4>
             Requirements for DRBC Donation
         </h4>
         <ul>
             <li>donation frequency: Every 112 days, up to 3 times/year</li>
             <li>must be in good health and feeling well</li>
             <li> donors must be at least 17 years old , at least 5'1" tall and weigh at least 59 Kgs

             </li>
             <li>
                 donors must be at least 19 years old, at least 5'5" tall and weigh at least 69 Kgs
             </li>
             <li>
            donors must also have a hemoglobin level of at least 13.3 g/dL
             </li>
         </ul>
         
